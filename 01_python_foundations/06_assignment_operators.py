# Assignment Operators


"""
Operator	    Meaning	        Example

=	            Assign	        x = 5
+=	        Add and assign	    x += 3
-=	    Subtract and assign	    x -= 3
*=	    Multiply and assign	    x *= 3
/=	      Divide and assign	    x /= 3
//=	        Floor divide	    x //= 2
%=	        Modulo	            x %= 2
**=	        Exponent	        x **= 2

"""

replicas = 2
replicas += 3  # replicas = replicas + 3

print("Updated replicas:", replicas)
x = 5 
