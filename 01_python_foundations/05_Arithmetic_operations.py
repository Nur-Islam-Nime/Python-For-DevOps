    # 1. Arithmetic Operators:

cpu_cores = 8 
containers = 3

# Addition
print("Total units (CPU + Containers):", cpu_cores + containers)

# Subtraction
print("Remaining cores after assigning:", cpu_cores - containers)

# Multiplication
print("Total vCPUs needed:", cpu_cores * containers)

# Division
print("CPU per container:", cpu_cores / containers)

# Floor Division
print("Whole cores per container:", cpu_cores // containers)

# Modulus
print("Unassigned cores:", cpu_cores % containers)

# Exponent
print("CPU squared:", cpu_cores ** 2)

